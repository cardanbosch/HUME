      if nargin > 0
          
      setdbprefs('DataReturnFormat','Table');  
      
      c = getappdata(0,'Conn');
      if strcmp(c.Driver, 'org.sqlite.JDBC')
          db = '';
%          sl = 1;
          
      else
          db = 'SleepLabStats.';
      end
      
      [statistics] =  fetch(getappdata(0,'Conn'), ['SELECT ', ...
            '', db, 'idStudy.id, ', ...
            '', db, 'idStudy.study, ', ...
            '', db, 'EDFInfo.EDFname, ', ...
            '', db, 'EDFInfo.condition, ', ...
            '', db, 'scoredInfo.scorer, ', ...
            '', db, 'scoredInfo.record, ', ...
            '', db, 'TDT.Minutes AS TDTmin, ', ...
            '', db, 'SPT.Minutes AS SPTmin, ', ...
            '', db, 'SPT.TDTper AS [SPT_TDTper], ', ...
            '', db, 'TST.Minutes AS TSTmin, ', ...
            '', db, 'TST.TDTper AS [TST_TDTper], ', ...
            '', db, 'TST.SPTper AS [TST_SPTper], ', ...
            '', db, 'WAFA.Minutes AS WAFAmin, ', ...
            '', db, 'WASO.Minutes AS WASOmin, ', ...
            '', db, 'latencySleep.Minutes AS SleepLatency, ', ...
            '', db, 'latencySOREM.Minutes AS REML, ', ...
            '', db, 'TWT.Minutes AS TWTmin, ', ...
            '', db, 'TWT.TSTper AS [TWT_TSTper], ', ...
            '', db, 'Stage1.Minutes AS [St1_min], ', ...
            '', db, 'Stage1.TSTper AS [St1_TSTper], ', ...
            '', db, 'Stage2.Minutes AS [St2_min], ', ...
            '', db, 'Stage2.TSTper AS [St2_TSTper], ', ...
            '', db, 'Stage3.Minutes AS [St3_min], ', ...
            '', db, 'Stage3.TSTper AS [St3_TSTper], ', ...
            '', db, 'Stage4.Minutes AS [St4_min], ', ...
            '', db, 'Stage4.TSTper AS [St4_TSTper], ', ...
            '', db, 'REM.Minutes AS [REM_min], ', ...
            '', db, 'REM.TSTper AS [REM_TSTper], ', ...
            '', db, 'MT.Minutes AS [MT_min], ', ...
            '', db, 'MT.TSTper AS [MT_TSTper], ', ...
            '', db, 'SW.Minutes AS [SW_min], ', ...
            '', db, 'SW.TSTper AS [SW_TSTper], ', ...
            '', db, 'NREM.Minutes AS [NREM_min], ', ...
            '', db, 'NREM.TSTper AS [NREM_TSTper] ', ...
            'FROM ', ...
            '', db, 'EDFInfo ', ...
            'JOIN ', db, 'idStudy ', ...
            'ON ', db, 'EDFInfo.id = ', db, 'idStudy.id  ', ...
            'JOIN ', db, 'scoredInfo ', ...
            'ON ', db, 'scoredInfo.EDFname = ', db, 'EDFInfo.EDFname  ', ...
            'JOIN ', db, 'TST ', ...
            'ON ', db, 'TST.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'SPT ', ...
            'ON ', db, 'SPT.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'TDT ', ...
            'ON ', db, 'TDT.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'WAFA ', ...
            'ON ', db, 'WAFA.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'WASO ', ...
            'ON ', db, 'WASO.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'latencySleep ', ...
            'ON ', db, 'latencySleep.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'latencySOREM ', ...
            'ON ', db, 'latencySOREM.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'TWT ', ...
            'ON ', db, 'TWT.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'Stage1 ', ...
            'ON ', db, 'Stage1.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'Stage2 ', ...
            'ON ', db, 'Stage2.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'Stage3 ', ...
            'ON ', db, 'Stage3.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'Stage4 ', ...
            'ON ', db, 'Stage4.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'REM ', ...
            'ON ', db, 'REM.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'MT ', ...
            'ON ', db, 'MT.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'SW ', ...
            'ON ', db, 'SW.record = ', db, 'scoredInfo.record  ', ...
            'JOIN ', db, 'NREM ', ...
            'ON ', db, 'NREM.record = ', db, 'scoredInfo.record ', ...
            'WHERE ', db, 'scoredInfo.record IN (',strjoin(strcat('''',recordList,''''),', '),');']);
            setdbprefs('DataReturnFormat','Table');  
            
%             if sl
%                 statistics = cell2table(statistics, 'VariableNames', {'id','study','EDFname','condition','scorer','record','TDTmin','SPTmin','SPT_TDTper','TSTmin','TST_TDTper','TST_SPTper','WAFAmin','WASOmin','SleepLatency','REML','TWTmin','TWT_TSTper','St1_min','St1_TSTper','St2_min','St2_TSTper','St3_min','St3_TSTper','St4_min','St4_TSTper','REM_min','REM_TSTper','MT_min','MT_TSTper','SW_min','SW_TSTper','NREM_min','NREM_TSTper'});
%             end
                
      end
end
